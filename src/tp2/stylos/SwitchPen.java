
package tp2.stylos;

import java.awt.Color;

public class SwitchPen extends Stylo {
	
	public SwitchPen() {
		super(Color.GREEN);
	}
	
	public void write(String s, char a, char b) {
		this.write(s.replace(a, b));
	}
}

